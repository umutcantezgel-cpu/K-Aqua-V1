const K_PAGES_I18N = { de: {}, en: {}, ar: {} };

function kDeepMerge(base, over) {
  if (over === undefined) return base;
  if (Array.isArray(base) || typeof base !== 'object' || base === null) return over;
  const out = {};
  Object.keys(base).forEach((k) => { out[k] = kDeepMerge(base[k], over ? over[k] : undefined); });
  return out;
}

function usePageL(key) {
  const { lang } = useT();
  return React.useMemo(() => {
    const de = K_PAGES_I18N.de[key] || {};
    if (lang === 'de') return de;
    const en = kDeepMerge(de, K_PAGES_I18N.en[key]);
    if (lang === 'en') return en;
    return kDeepMerge(en, (K_PAGES_I18N[lang] || {})[key]);
  }, [key, lang]);
}

